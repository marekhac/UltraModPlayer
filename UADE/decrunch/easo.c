int easo_hack (FILE *f, FILE *fo)
{
    uint8 *packed, *unpacked;
    int plen, unplen;
    struct stat st;

    if (fo == NULL)
	return -1;

    fstat (fileno (f), &st);
    plen = st.st_size;

    packed = malloc (plen);
    if (!packed)
	{
	fprintf(stderr, "can't allocate memory for EarAche hack");
	return -1;
    	}

    fread (packed, plen,1 ,f); /* read easo data */

    unplen = 1166 + plen - 4; 

    unpacked = (uint8 *) malloc (unplen);
    if (!unpacked)
     {
     fprintf (stderr, "can't allocate mem  for EarAche hack");
     return -1;	/* no valid data found */
     }

    memcpy (unpacked,EarAche_header,1166);  /* copy header*/
    memcpy (unpacked+1166,packed+4,plen-4); /* copy musicdata*/

    fwrite (unpacked, unplen, 1, fo);
    free (unpacked);
    free (packed);

    return 0;
}

